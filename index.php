<p>Bootcake is a modified version of Usercake (http://usercake.com/)  </p>
<p>Usercake is 100% free and open source and so is Bootcake.</p>
<p> Bootcake uses the core framework of Usercake The main difference is I've modified Bootcake to use Bootstrap CSS and JQuery. I've also added a new PHP class called class.main.php where you can put your additional PHP logic. It's in the includes folder which has a second OOP database class. You can either use the original Usercake procedural structure going forward or mine, both still work it's just a matter of preference and how well you understand PHP security issues. I've also added some examples of how to use PHP and AJAX to get data from the database into a format that you can easily use with JavaScript on the front end. </p>
<p>Some other things I've incorporated are:</p>
